function Prediction() {
  const [selectedSymptoms, setSelectedSymptoms] = useState([]);

  const [prediction, setPrediction] = useState(null);

  const [search, setSearch] = useState("");

  const [loading, setLoading] = useState(false);
 const [symptoms, setSymptoms] = useState([]);
  const navigate = useNavigate();

  useEffect(() => {

    fetch("https://arogyaai-backend-wudu.onrender.com/symptoms")

        .then(res => res.json())

        .then(data => setSymptoms(data));

}, []);
  

  const downloadPDF = () => {
    if (!prediction) {
      toast.error("No prediction available");
      return;
    }


    const doc = new jsPDF();

    const pageWidth = doc.internal.pageSize.getWidth();

    // ===========================
    // HEADER
    // ===========================

    doc.setFillColor(25, 118, 210);
    doc.rect(0, 0, pageWidth, 30, "F");

    doc.setTextColor(255, 255, 255);
    doc.setFont("helvetica", "bold");
    doc.setFontSize(22);

    doc.text("AROGYAAI", 20, 15);

    doc.setFontSize(11);

    doc.text("AI Powered Healthcare Assistant", 20, 23);

    // ===========================
    // REPORT TITLE
    // ===========================

    doc.setTextColor(0);

    doc.setFontSize(18);

    doc.text("Medical Prediction Report", 20, 42);

    // ===========================
    // REPORT ID
    // ===========================

    const reportId = "AR-" + Date.now();

    const today = new Date();

    doc.setFontSize(11);

    doc.text(`Report ID : ${reportId}`, 20, 52);

    doc.text(`Date : ${today.toLocaleDateString()}`, 140, 52);

    doc.text(`Time : ${today.toLocaleTimeString()}`, 140, 60);

    // ===========================
    // PATIENT DETAILS
    // ===========================

    doc.setFillColor(240, 248, 255);

    doc.roundedRect(15, 68, 180, 32, 3, 3, "F");

    doc.setFontSize(13);

    doc.setFont("helvetica", "bold");

    doc.text("Patient Details", 20, 78);

    doc.setFont("helvetica", "normal");

    doc.setFontSize(11);

    doc.text(`Name : ${localStorage.getItem("name")}`, 20, 88);

    doc.text(`Email : ${localStorage.getItem("email")}`, 20, 96);

    // ===========================
    // PREDICTION RESULT
    // ===========================

    let y = 115;

    const drawSection = (title, value, color) => {
      doc.setFillColor(...color);

      doc.roundedRect(15, y, 180, 18, 2, 2, "F");

      doc.setTextColor(255);

      doc.setFont("helvetica", "bold");

      doc.text(title, 20, y + 11);

      y += 22;

      doc.setTextColor(0);

      doc.setFont("helvetica", "normal");

      doc.setFontSize(12);

      doc.text(value || "N/A", 22, y);

      y += 15;
    };

    drawSection("Disease", prediction.disease, [33, 150, 243]);

    drawSection("Severity", prediction.severity, [244, 67, 54]);

    drawSection("Recommended Doctor", prediction.doctor, [76, 175, 80]);

    drawSection("Medicine", prediction.medicine, [156, 39, 176]);

    drawSection("Diet Plan", prediction.diet, [255, 152, 0]);

    drawSection("Precautions", prediction.precaution, [121, 85, 72]);

    drawSection("Health Alert", prediction.alert, [211, 47, 47]);

    // ===========================
    // DISCLAIMER
    // ===========================

    doc.setFillColor(255, 245, 220);

    doc.roundedRect(15, y + 5, 180, 30, 3, 3, "F");

    doc.setFont("helvetica", "bold");

    doc.setFontSize(12);

    doc.text("Disclaimer", 20, y + 16);

    doc.setFont("helvetica", "normal");

    doc.setFontSize(10);

    doc.text(
      "This report is AI-generated. Please consult a qualified doctor",

      20,

      y + 24,
    );

    doc.text(
      "before taking any medication or medical decision.",

      20,

      y + 30,
    );

    // ===========================
    // FOOTER
    // ===========================

    doc.setDrawColor(180);

    doc.line(15, 285, 195, 285);

    doc.setFontSize(10);

    doc.text(
      "Generated by ArogyaAI | AI Powered Healthcare Assistant",
      20,
      292,
    );

    doc.text("© 2026 ArogyaAI", 150, 292);

    doc.save(`ArogyaAI_Report_${reportId}.pdf`);
  };

  const openNearbyHospitals = () => {

    if (!navigator.geolocation) {

        alert("Geolocation is not supported.");

        return;

    }

    navigator.geolocation.getCurrentPosition(

        (position) => {

            const latitude = position.coords.latitude;

            const longitude = position.coords.longitude;

            window.open(

                `https://www.google.com/maps/search/hospital/@${latitude},${longitude},15z`,

                "_blank"

            );

        },

        () => {

            alert("Location permission denied.");

        }

    );

};

  const { transcript, listening, resetTranscript } = useSpeechRecognition();

  const getSeverityColor = (severity) => {
    switch (severity) {
      case "Critical":
        return "bg-red-600 text-white";

      case "High":
        return "bg-orange-500 text-white";

      case "Medium":
        return "bg-yellow-400 text-black";

      case "Low":
        return "bg-green-500 text-white";

      default:
        return "bg-blue-600 text-white";
    }
  };

  const predictDisease = async () => {
    let symptomList = [...selectedSymptoms];

if (transcript) {

    const voiceSymptoms = symptoms.filter(symptom =>

        transcript
            .toLowerCase()
            .includes(symptom.toLowerCase())

    );

    voiceSymptoms.forEach(symptom => {

        if (!symptomList.includes(symptom)) {

            symptomList.push(symptom);

        }

    });

}

    if (symptomList.length === 0) {
      toast.error("Please select symptoms");

      return;
    }

    try {
      setLoading(true);

      const response = await fetch(
        "https://arogyaai-backend-wudu.onrender.com/predict",

        {
          method: "POST",

          headers: {
            "Content-Type": "application/json",
          },

          body: JSON.stringify({
            symptoms: symptomList,

            email: localStorage.getItem("email"),
          }),
        },
      );

      const data = await response.json();

      setPrediction(data);

      resetTranscript();

      toast.success("Prediction Successful");
    } catch {
      toast.error("Prediction Failed");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="max-w-6xl mx-auto px-6 py-10">
      <div className="bg-gradient-to-br from-white to-blue-50 rounded-3xl shadow-2xl p-10">
        <div className="flex justify-between items-start mb-8">

  {/* Left Side */}
  <div className="flex items-center gap-4">

    <div className="text-6xl">🩺</div>

    <div>

      <h1 className="text-5xl font-bold text-blue-700">
        Disease Prediction
      </h1>

      <p className="text-gray-500 mt-2 text-lg">
        Select symptoms manually or use voice.
      </p>

    </div>

  </div>

  {/* Right Side */}
  <button
    onClick={() => navigate("/")}
    className="
      bg-blue-600
      hover:bg-blue-700
      text-white
      px-8
      py-3
      rounded-2xl
      shadow-lg
      transition
      font-medium
    "
  >
    🏠 Back Home
  </button>

</div>

        <div className="flex gap-4 mb-8">
          <input
            type="text"
            placeholder="🔍 Search Symptoms..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="

flex-1

border

border-gray-300

rounded-2xl

p-4

outline-none

focus:ring-4

focus:ring-blue-200

"
          />

          <button
            onClick={() => {
              if (listening) {
                SpeechRecognition.stopListening();
              } else {
                SpeechRecognition.startListening({
                  continuous: true,
                });
              }
            }}
            className="

px-8

rounded-2xl

bg-green-600

hover:bg-green-700

text-white

shadow-lg

transition

"
          >
            {listening ? "🔴 Listening" : "🎤 Voice"}
          </button>
        </div>

        <div className="grid grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-4">
          {symptoms
  .filter((symptom) =>
    symptom.toLowerCase().includes(search.toLowerCase())
  )
  .slice(0, 20)

            .map((symptom) => (
              <button
                key={symptom}
                onClick={() => {
                  if (selectedSymptoms.includes(symptom)) {
                    setSelectedSymptoms(
                      selectedSymptoms.filter((item) => item !== symptom),
                    );
                  } else {
                    setSelectedSymptoms([...selectedSymptoms, symptom]);
                  }
                }}
                className={`

rounded-full

px-5

py-3

font-medium

transition-all

duration-300

hover:scale-105

${
  selectedSymptoms.includes(symptom)
    ? "bg-blue-600 text-white shadow-lg"
    : "bg-white border hover:bg-blue-50"
}

`}
              >
                {symptom}
              </button>
            ))}
        </div>

        {selectedSymptoms.length > 0 && (
          <div className="mt-8">
            <div className="flex justify-between">
              <h3 className="font-bold text-lg">Selected Symptoms</h3>

              <span className="text-blue-600">
                {selectedSymptoms.length} Selected
              </span>
            </div>

            <div className="flex flex-wrap gap-3 mt-4">
              {selectedSymptoms.map((symptom) => (
                <div
                  key={symptom}
                  className="

bg-blue-600

text-white

rounded-full

px-4

py-2

flex

items-center

gap-2

"
                >
                  {symptom}

                  <button
                    onClick={() => {
                      setSelectedSymptoms(
                        selectedSymptoms.filter((item) => item !== symptom),
                      );
                    }}
                  >
                    ✕
                  </button>
                </div>
              ))}
            </div>
          </div>
        )}

        {transcript && (
          <div className="mt-8 bg-blue-50 border border-blue-100 rounded-2xl p-6">
            <h3 className="font-bold text-blue-700">🎤 Voice Input</h3>

            <p className="mt-2">{transcript}</p>
          </div>
        )}

        <button
          onClick={predictDisease}
          disabled={loading}
          className="

mt-8

w-full

bg-gradient-to-r

from-blue-600

to-cyan-600

text-white

rounded-2xl

py-5

text-xl

font-bold

shadow-xl

hover:scale-[1.02]

transition-all

"
        >
          {loading ? "⏳ Predicting Disease..." : "🩺 Predict Disease"}
        </button>
      </div>

      {prediction && (
        <div
          className="
          mt-10
          bg-white
          rounded-3xl
          shadow-2xl
          p-8
          border
          border-blue-100
          animate-fade-in
          "
        >
          {/* Header */}

          <div className="flex justify-between items-center mb-8">
            <div>
              <h2 className="text-4xl font-bold text-blue-700">
                {prediction.disease}
              </h2>

              <p className="text-gray-500 mt-1">AI Prediction Result</p>
            </div>

            {prediction.confidence && (

<div className="mt-3">

<div className="flex justify-between text-sm mb-1">

<span>Confidence</span>

<span>{prediction.confidence}%</span>

</div>

<div className="w-full bg-gray-200 rounded-full h-3">

<div

className="bg-green-600 h-3 rounded-full"

style={{

width:`${prediction.confidence}%`

}}

></div>

</div>

</div>

)}

            <span
              className={`

              px-6

              py-3

              rounded-full

              font-bold

              text-lg

              ${getSeverityColor(prediction.severity)}

              `}
            >
              {prediction.severity}
            </span>
          </div>

          {/* Cards */}

          <div className="grid md:grid-cols-2 gap-6">
            <div className="bg-blue-50 rounded-2xl p-6 hover:shadow-lg transition">
              <div className="text-4xl mb-3">👨‍⚕️</div>

              <h3 className="font-bold text-xl">Recommended Doctor</h3>

              <p className="mt-3 text-gray-700">{prediction.doctor}</p>
            </div>

            <div className="bg-green-50 rounded-2xl p-6 hover:shadow-lg transition">
              <div className="text-4xl mb-3">💊</div>

              <h3 className="font-bold text-xl">Medicine</h3>

              <p className="mt-3 text-gray-700">{prediction.medicine}</p>
            </div>

            <div className="bg-yellow-50 rounded-2xl p-6 hover:shadow-lg transition">
              <div className="text-4xl mb-3">🥗</div>

              <h3 className="font-bold text-xl">Recommended Diet</h3>

              <p className="mt-3 text-gray-700">{prediction.diet}</p>
            </div>

            <div className="bg-purple-50 rounded-2xl p-6 hover:shadow-lg transition">
              <div className="text-4xl mb-3">⚠️</div>

              <h3 className="font-bold text-xl">Precautions</h3>

              <p className="mt-3 text-gray-700">{prediction.precaution}</p>
            </div>
          </div>

          {/* Alert */}

          <div className="mt-8 bg-red-50 border-l-4 border-red-600 rounded-xl p-6">
            <h3 className="text-xl font-bold text-red-700">🚨 Health Alert</h3>

            <p className="mt-2 text-gray-700">{prediction.alert}</p>
          </div>

          {/* Action Buttons */}

          <div className="mt-10 flex flex-wrap gap-4 justify-center">
            <button
              onClick={downloadPDF}
              className="
  bg-blue-600
  hover:bg-blue-700
  text-white
  px-6
  py-3
  rounded-xl
  shadow-lg
  "
            >
              📄 Download Report
            </button>

            <button
              onClick={() => navigate("/appointment")}
              className="
bg-green-600
hover:bg-green-700
text-white
px-6
py-3
rounded-xl
shadow-lg
"
            >
              📅 Book Appointment
            </button>

            <button
onClick={openNearbyHospitals}
              className="
bg-red-600
hover:bg-red-700
text-white
px-6
py-3
rounded-xl
shadow-lg
transition
"
            >
              📍 Nearby Hospital
            </button>
          </div>
        </div>
      )}
    </div>
  );
}

export default Prediction;
